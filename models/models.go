package models

import (
	"encoding/hex"
	"os"
	"path"

	"github.com/Unknwon/com"
	"github.com/astaxie/beego/orm"
	_ "github.com/mattn/go-sqlite3"
)

const (
	_DB_NAME        = "data/beeblog.db"
	_SQLITE3_DRIVER = "sqlite3"
)

type UUID [16]byte

func (u *UUID) Bytes() []byte {
	return u[:]
}

